void Read_Tempurature(void *argument)
{
  /* USER CODE BEGIN Read_Tempurature */
	//declarations
	uint8_t data[2];
	uint8_t uart_buf[40];
	uint16_t temp;
  /* Infinite loop */
    for(;;)
    {
	    //Receive temperature data via SPI
	    HAL_GPIO_WritePin(CS_MAX6675_Port, CS_MAX6675_Pin, GPIO_PIN_RESET);
	    HAL_SPI_Receive(&hspi1, data, 1, HAL_MAX_DELAY);
	    HAL_GPIO_WritePin(CS_MAX6675_Port, CS_MAX6675_Pin, GPIO_PIN_SET);

	    //conversion to degree C
	    temp = ((((data[0]|data[1]<<8)))>>3);
	    float temperature = temp * 0.25;

	    // String conversion
	    temperature *= 100;
	    sprintf((char*)uart_buf, "Task2: Temperature: %u.%02u C\r\n",
	    		((unsigned int) temperature/100), ((unsigned int) temperature%100));
	    //Transmit via UART
	    HAL_UART_Transmit(&huart3, uart_buf, strlen((char*)uart_buf), HAL_MAX_DELAY);

	    osDelay(2000);
    }
    //In case we accidently exit the task loop
    osThreadTerminate(NULL);
  /* USER CODE END Read_Tempurature */
}

